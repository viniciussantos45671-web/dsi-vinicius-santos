package aula05;

public class Operadores_de_atribuição {

    public static void main(String[] args) {

        int x = 10;
        x += 5; // x = x + 5
        System.out.println("x = " + x);

        int y = 20;
        y -= 3; // y = y - 3
        System.out.println("y = " + y);

        int w = 16;
        w /= 4; // w = w / 4
        System.out.println("w = " + w);

        int z = 10;
        z %= 3; // z = z % 3
        System.out.println("z = " + z);

        int a = 2;
        a *= 2; // a = a * 2
        System.out.println("a = " + a);
    }
}